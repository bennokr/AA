package com.uva.aa.enums;

/**
 * Denotes the different possible types of states the game can be in.
 */
public enum GameState {
	
	/** Indicates that the game hasn't started yet */
	PREPARATION,
	
	/** Indicates that the game is currently active */
	RUNNING,
	
	/** Indicates that the game has finished */
	FINISHED;
}
